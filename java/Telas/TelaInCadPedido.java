    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (tfDatap.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "A data do pagamento é obrigatória!");
            tfDatap.requestFocus();
            return;
        }
        if (tfStatus.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O Status é obrigatório!");
            tfStatus.requestFocus();
            return;
        }

        if (tfFormp.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "A forma de pagamento é obrigatória!");
            tfFormp.requestFocus();
            return;
        }

        String dataPedido = tfDatap.getText();
        String status = tfStatus.getText();
        String formaPag = tfFormp.getText();
        String obs = tfObs.getText();

        Pedido p = new Pedido();
        p.setDataPedido(dataPedido);
        p.setStatus(status);
        p.setFormaPag(formaPag);
        p.setObs(obs);

        boolean retorno = tp.salvar(p);

        if (retorno) {
            JOptionPane.showMessageDialog(null, "Salvo com sucesso");
            tfDatap.setText("");
            tfStatus.setText("");
            tfFormp.setText("");
            tfObs.setText("");

            tfDatap.requestFocus();
            montaTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro, verifique os logs.");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

