    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String idString = String.valueOf(tblPedido.getValueAt(tblPedido.getSelectedRow(), 0));
        int id = Integer.parseInt(idString);

        boolean retorno = tp.excluir(id);
        if (retorno) {
            JOptionPane.showMessageDialog(null, "Registro excluído com sucesso!");
            montaTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao excluir!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void montaTabela() {
        ArrayList<Pedido> p = tp.recuperarTodos();
        if (p == null) {
        } else {
            tblPedido.setModel(new AbstractTableModel() {
                @Override
                public String getColumnName(int column) {
                    switch (column) {
                        case 0:
                            return "Código";
                        case 1:
                            return "Data Pedido";
                        case 2:
                            return "Status";
                        case 3:
                            return "Forma Pagamento";
                        case 4:
                            return "Observacao";
                        default:
                            return "";
                    }
                }

                @Override
                public int getColumnCount() {
                    return 5;
                }

                @Override
                public int getRowCount() {
                    return p.size();
                }

                @Override
                public Object getValueAt(int rowIndex, int columnIndex) {
                    Pedido ps = p.get(rowIndex);

                    if (ps != null) {
                        switch (columnIndex) {
                            case 0:
                                return ps.getId();
                            case 1:
                                return ps.getDataPedido();
                            case 2:
                                return ps.getStatus();
                            case 3:
                                return ps.getFormaPag();
                            case 4:
                                return ps.getObs();
                        }

                    }

                    return "n/d";
                }
            });

            tblPedido.getColumnModel().getColumn(0).setMinWidth(80);
            tblPedido.getColumnModel().getColumn(0).setPreferredWidth(80);
            tblPedido.getColumnModel().getColumn(0).setMaxWidth(20);
        }
    }

