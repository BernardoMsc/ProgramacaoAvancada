    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String idString = String.valueOf(tblCliente.getValueAt(tblCliente.getSelectedRow(), 0));
        int id = Integer.parseInt(idString);

        boolean retorno = tc.excluir(id);
        if (retorno) {
            JOptionPane.showMessageDialog(null, "Registro excluído com sucesso!");
            montaTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao excluir!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (tfNome.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O nome é obrigatório!");
            tfNome.requestFocus();
            return;
        }
        if (tfTelefone.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O telefone é obrigatório!");
            tfTelefone.requestFocus();
            return;
        }

        if (tfCpf.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O CPF é obrigatório!");
            tfCpf.requestFocus();
            return;
        }

        if (tfEndereco.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O endereço é obrigatório!");
            tfEndereco.requestFocus();
            return;
        }

        String nome = tfNome.getText();
        String telefone = tfTelefone.getText();
        String cpf = tfCpf.getText();
        String endereco = tfEndereco.getText();

        Cliente c = new Cliente();
        c.setNome(nome);
        c.setTelefone(telefone);
        c.setCpf(cpf);
        c.setEndereco(endereco);

        boolean retorno = tc.salvar(c);

        if (retorno) {
            JOptionPane.showMessageDialog(null, "Salvo com sucesso");
            tfNome.setText("");
            tfTelefone.setText("");
            tfCpf.setText("");
            tfEndereco.setText("");

            tfNome.requestFocus();
            montaTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro, verifique os logs.");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

