    private void montaTabela() {
        ArrayList<Cliente> c = tc.recuperarTodos();
        if (c == null) {
        } else {
            tblCliente.setModel(new AbstractTableModel() {
                @Override
                public String getColumnName(int column) {
                    switch (column) {
                        case 0:
                            return "Código";
                        case 1:
                            return "Nome";
                        case 2:
                            return "Telefone";
                        case 3:
                            return "CPF";
                        case 4:
                            return "Endereço";
                        default:
                            return "";
                    }
                }

                @Override
                public int getColumnCount() {
                    return 5;
                }

                @Override
                public int getRowCount() {
                    return c.size();
                }

                @Override
                public Object getValueAt(int rowIndex, int columnIndex) {
                    Cliente cs = c.get(rowIndex);

                    if (cs != null) {
                        switch (columnIndex) {
                            case 0:
                                return cs.getId();
                            case 1:
                                return cs.getNome();
                            case 2:
                                return cs.getTelefone();
                            case 3:
                                return cs.getCpf();
                            case 4:
                                return cs.getEndereco();
                        }

                    }

                    return "n/d";
                }
            });

            tblCliente.getColumnModel().getColumn(0).setMinWidth(80);
            tblCliente.getColumnModel().getColumn(0).setPreferredWidth(80);
            tblCliente.getColumnModel().getColumn(0).setMaxWidth(20);
        }
    }


