    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String idString = String.valueOf(tblProduto.getValueAt(tblProduto.getSelectedRow(), 0));
        int id = Integer.parseInt(idString);

        boolean retorno = tpp.excluir(id);
        if (retorno) {
            JOptionPane.showMessageDialog(null, "Registro excluído com sucesso!");
            montaTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao excluir!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void montaTabela() {
        ArrayList<Produto> pp = tpp.recuperarTodos();
        if (pp == null) {
        } else {
            tblProduto.setModel(new AbstractTableModel() {
                @Override
                public String getColumnName(int column) {
                    switch (column) {
                        case 0:
                            return "Código";
                        case 1:
                            return "Descricao";
                        case 2:
                            return "Preco";
                        case 3:
                            return "Quantidade Estoque";
                        default:
                            return "";
                    }
                }

                @Override
                public int getColumnCount() {
                    return 4;
                }

                @Override
                public int getRowCount() {
                    return pp.size();
                }

                @Override
                public Object getValueAt(int rowIndex, int columnIndex) {
                    Produto pps = pp.get(rowIndex);

                    if (pps != null) {
                        switch (columnIndex) {
                            case 0:
                                return pps.getId();
                            case 1:
                                return pps.getDesc();
                            case 2:
                                return pps.getPreco();
                            case 3:
                                return pps.getQuantidade();
                        }

                    }

                    return "n/d";
                }
            });

            tblProduto.getColumnModel().getColumn(0).setMinWidth(80);
            tblProduto.getColumnModel().getColumn(0).setPreferredWidth(80);
            tblProduto.getColumnModel().getColumn(0).setMaxWidth(20);
        }
    }

