    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (tfPreco.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O preço é obrigatória!");
            tfPreco.requestFocus();
            return;
        }
        if (tfQnte.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "A quantidade é obrigatória!");
            tfQnte.requestFocus();
            return;
        }

        String desc = tfDesc.getText();
        double preco = Double.parseDouble(tfPreco.getText());
        int qnte = Integer.parseInt(tfQnte.getText());

        Produto pp = new Produto();
        pp.setDesc(desc);
        pp.setPreco(preco);
        pp.setQuantidade(qnte);

        boolean retorno = tpp.salvar(pp);

        if (retorno) {
            JOptionPane.showMessageDialog(null, "Salvo com sucesso");
            tfDesc.setText("");
            tfPreco.setText("");
            tfQnte.setText("");

            tfDesc.requestFocus();
            montaTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro, verifique os logs.");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

